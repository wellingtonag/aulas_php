<?php
    /*
•	== : Igual
•	=== : Idêntico (compara valor e tipo)
•	!= ou <> : Diferente
•	!== : Não idêntico
•	> : Maior que
•	< : Menor que
•	>= : Maior ou igual
•	<= : Menor ou igual
     */

     echo 5 == "5";
     echo "<br>";
     echo "abc" == "abc";

     
     echo "<br>";
     echo "---------------";
     echo "<br>";
    // identico - tipo e valor

     echo 5 === 5; 
     echo 5 === "5"; 
     echo 5 === "5";

     echo "<br>";
     echo "---------------";
     echo "<br>";
     
     //	!= ou <> : Diferente
    echo 5 !== 3 ;
    
    echo "<br>";
    echo "---------------";
    echo "<br>";

    echo 5 !== "5";

    echo "<br>";
    echo "---------------";
    echo "<br>";
    // > ou <

    echo 5 > 7;
    echo 5 < 7;

    echo "<br>";
    echo "---------------";
    echo "<br>";

    echo 5 >= 5;
?>